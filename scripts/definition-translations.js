function keyFor(ref, term) {
  return `${ref || ''}||${term}`;
}

function entryTranslation(entry) {
  const keyed = ENTRY_TRANSLATIONS[keyFor(entry.ref, entry.term)] || {};
  const termZh = keyed.termZh || TERM_TRANSLATIONS[entry.term] || entry.term;
  const definitionZh = DEFINITION_TRANSLATIONS[entry.term] || keyed.definitionZh;
  if (!definitionZh) {
    throw new Error(`Missing Chinese definition translation for: ${keyFor(entry.ref, entry.term)}`);
  }

  return { termZh, definitionZh };
}

